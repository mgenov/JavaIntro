package com.clouway.into;

/**
 * Sumator is a simple math class for that is used for adding of 2 numbers.
 *
 * @author David Kostov (david.kostov.cw@gmail.com)
 */
public class Sumator {

    /**
     * This method is used to sum two integers
     *
     * @param a the first integer to be summed
     * @param b the second integer to be summed
     * @return the sum of a and b
     */
    public int sum(int a, int b){
        return a+b;

    }



}
